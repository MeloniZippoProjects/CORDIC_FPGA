it_n = 32;

x = zeros(1,32);
y = zeros(1,32);
z = zeros(1,32);

x(1) = 2^32;
y(1) = 2^32;
z(1) = 0;

for i = 1:(it_n - 1)
    if(y(i) < 0)
        d = 1;
    else
        d = -1;
    end
    
    x(i+1) = x(i) - floor(y(i) * d * 2^(-(i-1)));
    y(i+1) = y(i) + floor(x(i) * d * 2^(-(i-1)));
    z(i+1) = z(i) - d * atan(2^(-(i-1)));
end    

format long;
disp('x: '); disp(x); disp(char(10));
disp('------------------------------------------------------------------------'); disp(char(10));
disp('y: '); disp(y); disp(char(10));
disp('------------------------------------------------------------------------'); disp(char(10));
disp('z: '); disp(z); disp(char(10));
disp('------------------------------------------------------------------------'); disp(char(10));